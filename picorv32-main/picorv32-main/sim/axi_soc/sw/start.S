	/* ----------------------------------------------------------------------
	 * start.S
	 * ----------------------------------------------------------------------
	 * 这是 AXI SoC demo 的最小启动文件，职责非常单一：
	 * 1) 设置栈指针 sp；
	 * 2) 跳转到 C 入口 main；
	 * 3) main 返回后进入 halt 死循环，防止跑飞。
	 *
	 * 说明：
	 * - 这里没有做 .data 拷贝、.bss 清零、中断向量表等完整启动流程，
	 *   因为当前 demo 只做最小功能验证。
	 * -------------------------------------------------------------------- */

	/* 把本段代码放到可执行代码段（由链接脚本安排到程序存储器起始地址） */
	.section .text.start

	/* 导出启动入口符号，链接器会把复位入口指向 _start */
	.global _start
	/* main 在 C 文件里定义，这里声明为全局符号便于 jal 跳转 */
	.global main

_start:
	/* sp = 0x2001_0000 的高 20 位；lui 先装载高位 */
	lui  sp, %hi(0x20010000)
	/* 再加低 12 位，得到完整栈顶地址：sp = 0x2001_0000 */
	addi sp, sp, %lo(0x20010000)

	/* 调用 main：
	 * - ra 保存返回地址（即下一条指令地址）
	 * - PC 跳到 main
	 */
	jal  ra, main

halt:
	/* main 若返回，会来到这里。
	 * ebreak 便于在仿真/调试中观察“程序结束点”。
	 */
	ebreak
	/* 无限循环，避免继续执行到未知区域 */
	j    halt
